#ifndef __LED_H
#define __LED_H

enum LED_Num {
	LED_1,
	LED_2,
	LED_3,
	LED_4,
	LED_5,
	LED_6
};

/**********************************************
* 初始化LED
**********************************************/
void LED_Init(void);

/**********************************************
* 开灯
**********************************************/
void LED_ON(enum LED_Num led);

/**********************************************
* 关灯
**********************************************/
void LED_OFF(enum LED_Num led);

/**********************************************
* 切换
**********************************************/
void LED_SWITCH(enum LED_Num led);

/**********************************************
* 开启全部LED
**********************************************/
void LED_ALL_ON(void);

/**********************************************
* 关闭全部LED
**********************************************/
void LED_ALL_OFF(void);

/**********************************************
* 闪烁某个LED
* 由50ms定时任务调用
**********************************************/
void LED_Blink(enum LED_Num led);


#endif
